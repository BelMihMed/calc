# Генерирует лендинги для блоков калькулятора на базе golos-bitrix24.html.
# CSS/разметка/логика сохраняются — заменяются только данные (modules/roles/pros/cases/demoData) и hero-тексты.
import re

TEMPLATE = open("golos-bitrix24.html", encoding="utf-8").read()

def build(out, title, eyebrow, demoQt, lede, about_lede, modules, roles, pros, cases, demo):
    s = TEMPLATE
    s = re.sub(r"const modules = \[.*?\n\];", lambda _: modules, s, count=1, flags=re.S)
    s = re.sub(r"const roles = \[.*?\n\];",   lambda _: roles,   s, count=1, flags=re.S)
    s = re.sub(r"const pros = \[.*?\n\];",     lambda _: pros,    s, count=1, flags=re.S)
    s = re.sub(r"const cases=\[.*?\n\];",      lambda _: cases,   s, count=1, flags=re.S)
    s = re.sub(r"const demoData=\[.*?\n\];",   lambda _: demo,    s, count=1, flags=re.S)
    s = s.replace("<title>Сценарии BitrixGPT 5.5 Agent</title>", "<title>" + title + "</title>")
    s = s.replace('<p class="eyebrow">Bitrix24 · ИИ-агенты</p>', '<p class="eyebrow">' + eyebrow + '</p>')
    s = s.replace('<span id="demoQt">напиши Петрову: документы готовы</span>', '<span id="demoQt">' + demoQt + '</span>')
    s = re.sub(r'<p class="lede">.*?</p>', lambda _: '<p class="lede">' + lede + '</p>', s, count=1, flags=re.S)
    s = re.sub(r'<p class="sec-lede">Чат-бот возвращает текст\..*?</p>',
               lambda _: '<p class="sec-lede">' + about_lede + '</p>', s, count=1, flags=re.S)
    open(out, "w", encoding="utf-8").write(s)
    print("Готово:", out, "(", len(s), "символов )")

